    private void cargarDatosTabla() {
    // Limpiar tabla antes de cargar
    DefaultTableModel modelo = (DefaultTableModel) TbUser.getModel();
    modelo.setRowCount(0); // Eliminar todas las filas
    
    Usuarios us = new Usuarios();
    java.util.List<Usuarios> listaUsuarios = us.obtenerTodosLosUsuarios();
    
    // Llenar la tabla con los datos
    for (Usuarios usuario : listaUsuarios) {
        Object[] fila = new Object[5];
        fila[0] = usuario.getNombre();
        fila[1] = usuario.getApellido();
        fila[2] = usuario.getCorreo();
        fila[3] = usuario.getRool();
        fila[4] = usuario.getUsername();
        
        modelo.addRow(fila);
    }
}
